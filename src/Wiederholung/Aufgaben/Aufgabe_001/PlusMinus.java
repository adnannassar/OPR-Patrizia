package Wiederholung.Aufgaben.Aufgabe_001;

public class PlusMinus {

    public static void main(String[] args) {
        int a = 1;
        int b = 2;
        System.out.println(++a);
        System.out.println(a);
        System.out.println(b++);
        System.out.println(b);
        System.out.println(a++ + "" + --b);
    }
}
