package Wiederholung.JavaDokumentation;

public class Main {
    /**
     * add adds two integers and returns the result
     *
     * @param a first integer
     * @param b second integer
     * @return c the sum of a and b
     */
    public static int add(int a, int b) {
        int c = a + b;
        return c;
    }
}
